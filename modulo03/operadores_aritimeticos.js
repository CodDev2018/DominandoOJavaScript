let x = 7
let y = 2

//Operadores Adção, Subtração, Multiplicação e Divisão
console.log('X + Y = ' + (x + y))
console.log('X - Y = ' + (x - y))
console.log('X * Y = ' + (x * y))
console.log('X / Y = ' + (x / y))

//Operador módulo
console.log('X % Y = ' + (x % y))

//Operador incremento
x = ++y
console.log('X = ++Y | X = ' + x + ' | Y = '+ y)
x = y++
console.log('X = Y++ | X = ' + x + ' | Y = '+ y)

//Operador decremento
x = --y
console.log('X = --Y | X = ' + x + ' | Y = '+ y)
x = y--
console.log('X = Y-- | X = ' + x + ' | Y = '+ y)